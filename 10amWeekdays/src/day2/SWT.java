package day2;

public class SWT {	
	public static void main(String[] args) {		
		int n=10;		
		switch (n) {
		case 1:
			System.out.println("one");
			break;
		case 2:
			System.out.println("two");
			break;
		case 3:
			System.out.println("three");
			break;
		default:
			System.out.println("Exit");
			break;
		}
	}
}
